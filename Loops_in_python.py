# Loops in Python
# What is a loop?
# A loop is a programming construct that allows you to repeat a block of code multiple times.
# There are two main types of loops in Python: for loops and while loops.

# Why use loops?
# Loops are useful for performing repetitive tasks without having to write the same code multiple times.

